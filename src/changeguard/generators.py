def write_artifacts(
    output_dir: str | Path,
    current: DatasetSchema,
    proposed: DatasetSchema,
    context: DatasetContext,
    result: AnalysisResult,
    source_relation: str,
) -> dict[str, Path]:
    target = Path(output_dir)
    target.mkdir(parents=True, exist_ok=True)
    paths = {
        "audit": target / "audit.json",
        "report": target / "report.md",
        "html": target / "report.html",
        "sql": target / "compatibility_view.sql",
        "dbt": target / "dbt_schema.yml",
        "context": target / "datahub_context.json",
    }
    write_json(paths["audit"], result.to_dict())
    write_json(paths["context"], context.to_dict())
    paths["sql"].write_text(
        render_compatibility_sql(current, proposed, result, source_relation), encoding="utf-8"
    )
    paths["dbt"].write_text(render_dbt_schema(proposed), encoding="utf-8")
    names = {key: path.name for key, path in paths.items()}
    paths["report"].write_text(render_markdown(result, context, names), encoding="utf-8")
    paths["html"].write_text(render_html(result, context), encoding="utf-8")
    return paths
