def _relation_from_urn(urn: str) -> str:
    match = re.fullmatch(r"urn:li:dataset:\(urn:li:dataPlatform:[^,]+,(.*),[^,]+\)", urn)
    return match.group(1) if match else "dataset"


def _yaml_scalar(value: str) -> str:
    return json.dumps(value, ensure_ascii=True)


def render_compatibility_sql(
    current: DatasetSchema,
    proposed: DatasetSchema,
    result: AnalysisResult,
    source_relation: str,
) -> str:
    proposed_names = {column.name for column in proposed.columns}
    unmapped = [
        column.name
        for column in current.columns
        if column.name not in proposed_names and column.name not in result.rename_hints
    ]
    header = [
        "-- Generated by DataHub ChangeGuard.",
        "-- Review and test this proposal before deployment.",
    ]
    if unmapped:
        header.extend(
            [
                "-- SQL generation stopped: the following removed fields have no safe mapping:",
                *[f"--   - {name}" for name in unmapped],
                "-- Add explicit rename hints or retain these fields during a deprecation window.",
                "",
            ]
        )
        return "\n".join(header)

    target_relation = f"{_relation_from_urn(current.dataset_urn)}_compat"
    projections = []
    for column in current.columns:
        replacement = result.rename_hints.get(column.name, column.name)
        if replacement == column.name:
            projections.append(f"    {replacement}")
        else:
            projections.append(f"    {replacement} AS {column.name}")

    return "\n".join(
        [
            *header,
            f"CREATE OR REPLACE VIEW {target_relation} AS",
            "SELECT",
            ",\n".join(projections),
            f"FROM {source_relation};",
            "",
        ]
    )


def render_dbt_schema(proposed: DatasetSchema) -> str:
    model_name = _relation_from_urn(proposed.dataset_urn).split(".")[-1]
    lines = [
        "version: 2",
        "",
        "models:",
        f"  - name: {_yaml_scalar(model_name)}",
        "    config:",
        "      contract:",
        "        enforced: true",
        "    columns:",
    ]
    for column in proposed.columns:
        lines.extend(
            [
                f"      - name: {_yaml_scalar(column.name)}",
                f"        data_type: {_yaml_scalar(column.native_type)}",
            ]
        )
        if column.description:
            lines.append(f"        description: {_yaml_scalar(column.description)}")
        if column.nullable is False:
            lines.extend(["        tests:", "          - not_null"])
    return "\n".join(lines) + "\n"


def render_markdown(
    result: AnalysisResult,
    context: DatasetContext,
    artifact_names: dict[str, str],
) -> str:
    owner_text = ", ".join(result.owners) if result.owners else "Unassigned"
    lines = [
        "# ChangeGuard schema review",
        "",
        f"- **Decision:** `{result.decision.value}`",
        f"- **Risk score:** `{result.risk_score}/100`",
        f"- **Dataset:** `{result.dataset_urn}`",
        f"- **Owners:** {owner_text}",
        f"- **Cataloged downstream assets:** `{result.downstream_count}`",
        f"- **Evidence source:** `{result.evidence_source}`",
        "",
        "## Findings",
        "",
        "| Severity | Code | Field | Finding |",
        "|---|---|---|---|",
    ]
    if result.findings:
        for finding in result.findings:
            escaped_summary = finding.summary.replace("|", "\\|")
            lines.append(
                f"| {finding.severity.value.upper()} | `{finding.code}` | `{finding.field}` | "
                f"{escaped_summary} |"
            )
    else:
        lines.append("| INFO | `NO_BREAKING_CHANGE` | - | No risky schema changes were detected. |")

    lines.extend(["", "## Evidence and remediation", ""])
    for index, finding in enumerate(result.findings, start=1):
        lines.extend(
            [
                f"### {index}. {finding.code}: `{finding.field}`",
                "",
                f"- Severity: **{finding.severity.value.upper()}**",
                f"- Evidence: {finding.evidence}",
                f"- Remediation: {finding.remediation}",
                "- Affected assets: "
                + (", ".join(f"`{urn}`" for urn in finding.affected_assets) or "None cataloged"),
                "",
            ]
        )

    lines.extend(
        [
            "## Generated proposals",
            "",
            f"- Compatibility SQL: `{artifact_names['sql']}`",
            f"- dbt contract: `{artifact_names['dbt']}`",
            f"- Machine-readable audit: `{artifact_names['audit']}`",
            "",
            "## Decision boundary",
            "",
            "ChangeGuard does not deploy generated code. A human or CI policy must review the evidence, "
            "run consumer tests, and approve the release. DataHub writeback is an explicit, separately "
            "confirmed action.",
            "",
        ]
    )
    return "\n".join(lines)


def _severity_badge(severity: Severity) -> str:
    return f'<span class="badge {severity.value}">{severity.value.upper()}</span>'


def _urn_display_name(value: str) -> str:
    return value.rsplit(":", 1)[-1].split("@", 1)[0]


